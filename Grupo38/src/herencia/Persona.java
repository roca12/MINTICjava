
package herencia;


public class Persona {
    String nombre;
    String rh;
    long cedula;
    char sexo;

    public Persona() {
    }

    public Persona(String nombre, String rh, long cedula, char sexo) {
        this.nombre = nombre;
        this.rh = rh;
        this.cedula = cedula;
        this.sexo = sexo;
    }
    
    
}
