
package herencia;


public class Persona {
    
    String nombre, nacionalidad;
    int altura,peso;
    String sexo;

    public Persona() {
    }

    public Persona(String nombre, String nacionalidad, int altura, int peso, String sexo) {
        this.nombre = nombre;
        this.nacionalidad = nacionalidad;
        this.altura = altura;
        this.peso = peso;
        this.sexo = sexo;
    }
    
    
    
}
